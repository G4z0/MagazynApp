String tr(String key, {Map<String, String>? args}) {
  final map = _translations[_currentLang] ?? _translations['pl']!;
  var text = map[key] ?? _translations['pl']?[key] ?? key;
  if (args != null) {
    args.forEach((k, v) => text = text.replaceAll('{$k}', v));
  }
  return text;
}

